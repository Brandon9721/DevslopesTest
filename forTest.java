import java.util.*;

public class forTest
{
	public static void main(String[] args)
	{
		for (int i = 0; i <=5; i++)
		{
			System.out.print(i + " \n");   //this code runs as long as Boolean Expression in 'for' body is true. 
			// as long as 1 <= 5 the code in these braces will execute
		}
		System.out.print("done\n");
	}
}